package z_homework;

public class Calculator {
	
	double add(double x, double y) {
		double result = x + y;
		return result;
	}

	double multipl(double x, double y) {
		double result = x * y;
		return result;
	}
	
	double divide(double x, double y) {
		double result = x / y;
		return result;
	}
	
	double minus(double x, double y) {
		double result = x - y;
		return result;
	}
	
	double remainder(double x, double y) {
		double result = x % y;
		return result;
	}
}
